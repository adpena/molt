def test_ci_gate_uses_repo_rustfmt_gate_without_compile_slot() -> None:
    spec = importlib.util.spec_from_file_location(
        "molt_test_ci_gate_for_rustfmt",
        ROOT / "tools" / "ci_gate.py",
    )
    assert spec is not None
    module = importlib.util.module_from_spec(spec)
    assert spec.loader is not None
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)

    check = {entry.name: entry for entry in module._build_checks()}["rustfmt"]

    assert check.cmd == [
        sys.executable,
        str(module.TOOLS / "check_rustfmt.py"),
        "--all",
    ]
    assert check.needs_rust is False
    assert check.needs_cargo is False
