from __future__ import annotations

import importlib.util
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
CHECK_RUSTFMT = ROOT / "tools" / "check_rustfmt.py"


def _load_check_rustfmt():
    spec = importlib.util.spec_from_file_location(
        "molt_test_check_rustfmt",
        CHECK_RUSTFMT,
    )
    assert spec is not None
    module = importlib.util.module_from_spec(spec)
    assert spec.loader is not None
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


def test_generated_rust_is_classified_by_header(tmp_path) -> None:
    tool = _load_check_rustfmt()
    generated = tmp_path / "table.rs"
    generated.write_text(
        "//! @generated by tools/gen_codecs.py from source.\n//! DO NOT EDIT.\n",
        encoding="utf-8",
    )
    human = tmp_path / "lib.rs"
    human.write_text("fn main() {}\n", encoding="utf-8")

    assert tool.is_generated_rust(generated) is True
    assert tool.is_generated_rust(human) is False
    assert tool.generated_owner(generated) == "python tools/gen_codecs.py --check"


def test_rustfmt_command_chunking_keeps_windows_command_lines_bounded() -> None:
    tool = _load_check_rustfmt()
    paths = tuple(Path(f"runtime/crate/src/{idx:04d}_module.rs") for idx in range(800))

    commands = tool._chunked_rustfmt_commands(
        "rustfmt",
        paths,
        check=True,
        edition="2024",
    )

    assert len(commands) > 1
    assert all(
        command[:4] == ["rustfmt", "--edition", "2024", "--check"]
        for command in commands
    )
    assert all(
        sum(len(part) + 1 for part in command) <= tool.WINDOWS_COMMAND_LINE_SOFT_LIMIT
        for command in commands
    )


def test_explicit_rust_paths_expand_files_and_directories(
    monkeypatch, tmp_path
) -> None:
    tool = _load_check_rustfmt()
    monkeypatch.setattr(tool, "ROOT", tmp_path)
    src = tmp_path / "runtime" / "crate" / "src"
    src.mkdir(parents=True)
    (src / "lib.rs").write_text("pub fn lib() {}\n", encoding="utf-8")
    (src / "README.md").write_text("not rust\n", encoding="utf-8")
    single = tmp_path / "tools" / "one.rs"
    single.parent.mkdir()
    single.write_text("fn one() {}\n", encoding="utf-8")

    paths = tool._explicit_rust_paths(["runtime/crate/src", "tools/one.rs"])

    assert paths == (
        Path("runtime/crate/src/lib.rs"),
        Path("tools/one.rs"),
    )


def test_write_mode_preserves_noop_crlf_bytes(monkeypatch, tmp_path) -> None:
    tool = _load_check_rustfmt()
    monkeypatch.setattr(tool, "ROOT", tmp_path)
    source = tmp_path / "src" / "main.rs"
    source.parent.mkdir()
    source.write_bytes(b'fn main() {\r\n    println!("ok");\r\n}\r\n')

    monkeypatch.setattr(
        tool,
        "_rustfmt_stdout",
        lambda _path, _edition: b'fn main() {\n    println!("ok");\n}\n',
    )

    assert tool._run_rustfmt((Path("src/main.rs"),), check=False, edition="2024") == 0
    assert source.read_bytes() == b'fn main() {\r\n    println!("ok");\r\n}\r\n'


