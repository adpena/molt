# Parity test: arithmetic operators
# All output via print() for diff comparison

print("=== Basic arithmetic ===")
print(2 + 3)
print(10 - 4)
print(3 * 7)
print(15 / 4)
print(15 // 4)
print(15 % 4)
print(2**10)
print(2**0)
print(0**0)

print("=== Negative operands ===")
print(-7 // 2)
print(-7 % 2)
print(7 // -2)
print(7 % -2)
print(-7 // -2)
print(-7 % -2)

print("=== Unary operators ===")
print(-5)
print(-(-5))
print(+5)
print(~0)
print(~1)
print(~(-1))
print(~255)

print("=== Bitwise operators ===")
print(0b1100 & 0b1010)
print(0b1100 | 0b1010)
print(0b1100 ^ 0b1010)
print(~0b1100)
print(1 << 10)
print(1024 >> 3)
print(0 & 0)
print(0 | 0)
print(0 ^ 0)
print(-1 & 0xFF)
print(-1 | 0)
print(-1 ^ 0)

print("=== Shift edge cases ===")
print(1 << 0)
print(1 << 1)
print(1 << 31)
print(1 << 63)
print(0 << 100)
print(128 >> 0)
print(128 >> 7)
print(128 >> 8)

print("=== Float arithmetic ===")
print(1.5 + 2.5)
print(1.0 / 3.0)
print(10.0 // 3.0)
print(10.0 % 3.0)
print(2.5**2)
print(-0.0 == 0.0)
print(repr(float("inf")))
print(repr(float("-inf")))
print(repr(float("nan")))
print(float("inf") + 1)
print(float("inf") - float("inf"))

print("=== Mixed int/float ===")
print(1 + 1.0)
print(2 * 3.5)
print(7 / 2)
print(7 // 2.0)
print(7 % 2.0)

print("=== Large integers ===")
print(2**100)
print(10**20)
print(10**20 + 1)
print(2**100 - 1)
print(2**100 // 3)

print("=== divmod ===")
print(divmod(17, 5))
print(divmod(-17, 5))
print(divmod(17, -5))
print(divmod(-17, -5))
print(divmod(10, 3))

print("=== pow with modulus ===")
print(pow(2, 10))
print(pow(2, 10, 1000))
print(pow(3, 4, 5))
print(pow(2, -1))

print("=== abs ===")
print(abs(0))
print(abs(-5))
print(abs(5))
print(abs(-3.14))
print(abs(3.14))

print("=== round ===")
print(round(2.5))
print(round(3.5))
print(round(1.5))
print(round(0.5))
print(round(-0.5))
print(round(-1.5))
print(round(2.675, 2))
print(round(1234, -2))
print(round(1250, -2))

print("=== Chained comparisons ===")
print(1 < 2 < 3)
print(1 < 2 > 1)
print(1 < 2 < 2)
print(1 <= 1 <= 1)
print(3 > 2 > 1)
print(3 > 2 > 2)

print("=== Boolean arithmetic ===")
print(True + True)
print(True * 10)
print(False + 1)
print(True + 0.5)
print(int(True))
print(int(False))
