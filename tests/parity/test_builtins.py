print("=== enumerate ===")
print(list(enumerate(["a", "b", "c"])))
print(list(enumerate(["x", "y"], start=1)))
print(list(enumerate([])))

print("=== zip ===")
print(list(zip([1, 2, 3], ["a", "b", "c"])))
print(list(zip([1, 2], [10, 20, 30])))
print(list(zip([], [1, 2])))
print(list(zip([1, 2, 3], [4, 5, 6], [7, 8, 9])))

print("=== zip strict ===")
print(list(zip([1, 2], [3, 4], strict=True)))
try:
    list(zip([1, 2], [3], strict=True))
except ValueError as e:
    print("zip strict error: ValueError")

print("=== map ===")
print(list(map(str, [1, 2, 3])))
print(list(map(lambda x: x * 2, [1, 2, 3])))
print(list(map(lambda x, y: x + y, [1, 2], [10, 20])))
print(list(map(abs, [-1, -2, 3, -4])))

print("=== filter ===")
print(list(filter(None, [0, 1, "", "a", [], [1], False, True])))
print(list(filter(lambda x: x > 0, [-2, -1, 0, 1, 2])))
print(list(filter(lambda x: x % 2 == 0, range(10))))

print("=== sorted ===")
print(sorted([3, 1, 4, 1, 5, 9]))
print(sorted([3, 1, 4, 1, 5, 9], reverse=True))
print(sorted(["banana", "apple", "cherry"]))
print(sorted(["banana", "apple", "cherry"], key=len))
print(sorted([(1, "b"), (2, "a"), (1, "a")]))

print("=== reversed ===")
print(list(reversed([1, 2, 3, 4, 5])))
print(list(reversed("hello")))
print(list(reversed(range(5))))
print(list(reversed((10, 20, 30))))

print("=== any / all ===")
print(any([False, False, True]))
print(any([False, False, False]))
print(any([]))
print(all([True, True, True]))
print(all([True, False, True]))
print(all([]))
print(any([0, 0, 1]))
print(all([1, 2, 3]))

print("=== sum ===")
print(sum([1, 2, 3]))
print(sum([1, 2, 3], 10))
