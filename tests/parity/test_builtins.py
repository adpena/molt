# Parity test: builtin functions
# All output via print() for diff comparison

print("=== print variations ===")
print()
print("hello")
print("a", "b", "c")
print("a", "b", "c", sep="-")
print("no newline", end="")
print(" continued")
print(1, 2.0, True, None, [1, 2])

print("=== len ===")
print(len([]))
print(len([1, 2, 3]))
print(len("hello"))
print(len({}))
print(len({1, 2, 3}))
print(len((1, 2)))
print(len(range(100)))

print("=== range ===")
print(list(range(5)))
print(list(range(2, 8)))
print(list(range(0, 10, 2)))
print(list(range(10, 0, -1)))
print(list(range(0, 0)))
