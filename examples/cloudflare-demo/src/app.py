def _parse_query(qs):
    """Parse a query string into a dict, handling edge cases."""
    result = {}
    if not qs:
        return result
    pairs = qs.split("&")
    i = 0
    while i < len(pairs):
        pair = pairs[i]
        eq_pos = pair.find("=")
        if eq_pos >= 0:
            key = pair[:eq_pos]
            val = pair[eq_pos + 1:]
            result[key] = val
        i = i + 1
    return result

def _strip_slashes(s):
    """Strip leading and trailing '/' characters from a string."""
    start = 0
    while start < len(s) and s[start] == "/":
        start = start + 1
    end = len(s)
    while end > start and s[end - 1] == "/":
        end = end - 1
    return s[start:end]

