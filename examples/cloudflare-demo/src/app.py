def fibonacci(n):
    a, b = 0, 1
    for _ in range(n):
        a, b = b, a + b
    return a

def is_prime(n):
    if n < 2: return False
    if n < 4: return True
    if n % 2 == 0 or n % 3 == 0: return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0: return False
        i += 6
    return True

def primes_up_to(limit):
    result = []
    n = 2
    while n <= limit:
        if is_prime(n):
            result.append(n)
        n = n + 1
    return result

def diamond(size=9):
    lines = []
    for i in range(size):
        d = i if i < size // 2 + 1 else size - 1 - i
        lines.append(" " * (size // 2 - d) + "*" * (2 * d + 1))
    return "\n".join(lines)

def mandelbrot_render(width: int = 120, height: int = 50,
                      cx: float = -0.5, cy: float = 0.0,
                      zoom: float = 1.0, max_iter: int = 80) -> str:
    chars: str = " .`'\"~:;!>+r}xz&8#@"
    scale: float = 3.0 / (zoom * width)
    x_off: float = cx - scale * width / 2.0
    y_off: float = cy - scale * height / 2.0
    out: list = []
    row: int = 0
    while row < height:
        y0: float = y_off + scale * row
        col: int = 0
        line: str = ""
        while col < width:
            x0: float = x_off + scale * col
            x: float = 0.0
            y: float = 0.0
            it: int = 0
            while it < max_iter:
                xx: float = x * x
                yy: float = y * y
                if xx + yy > 4.0:
                    break
                y = 2.0 * x * y + y0
                x = xx - yy + x0
                it = it + 1
            ci: int = it * (len(chars) - 1) // max_iter
            if ci >= len(chars):
                ci = len(chars) - 1
            line = line + chars[ci]
            col = col + 1
        out.append(line)
        row = row + 1
    return "\n".join(out)

def sort_data(data_str):
    nums = []
    bad = []
    for p in data_str.split(","):
        s = p.strip()
        if not s:
            continue
        try:
            nums.append(int(s))
        except (ValueError, TypeError):
            bad.append(s)
    return nums, bad

def fizzbuzz(n):
    lines = []
    for i in range(1, n + 1):
        if i % 15 == 0: lines.append("FizzBuzz")
        elif i % 3 == 0: lines.append("Fizz")
        elif i % 5 == 0: lines.append("Buzz")
        else: lines.append(str(i))
    return "\n".join(lines)

def safe_int(s, default, lo=0, hi=1000000):
    try:
        v = int(s)
    except (ValueError, TypeError):
        return default
    if v < lo:
        return lo
    if v > hi:
        return hi
    return v

def safe_float(s, default, lo=-1e15, hi=1e15):
    try:
        v = float(s)
    except (ValueError, TypeError):
        return default
    if v < lo:
        return lo
    if v > hi:
        return hi
    return v

def fmt_big(n):
    s = str(n)
    if len(s) <= 3:
        return s
    parts = []
    while len(s) > 3:
        parts.append(s[-3:])
        s = s[:-3]
    parts.append(s)
    parts.reverse()
    return ",".join(parts)

def truncate_num(s, digits=60):
    if len(s) <= digits:
        return s
    return s[:digits] + "... (" + fmt_big(len(s)) + " digits)"

