def fmt_big(n):
    s = str(n)
    if len(s) <= 3:
        return s
    parts = []
    while len(s) > 3:
        parts.append(s[len(s) - 3:])
        s = s[:len(s) - 3]
    parts.append(s)
    parts.reverse()
    return ",".join(parts)

