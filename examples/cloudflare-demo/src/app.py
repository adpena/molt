def _gpt_unpack():
    parts = _W.split(",")
    sd = {}
    idx = 0
    for shape in _GPT_SHAPES:
        key = shape[0]
        rows = shape[1]
        cols = shape[2]
        mat = []
        r = 0
        while r < rows:
            row = []
            c = 0
            while c < cols:
                row.append(float(parts[idx]))
                idx = idx + 1
                c = c + 1
            mat.append(row)
            r = r + 1
        sd[key] = mat
    return sd

def _gpt_linear(x, w):
    out = []
    for row in w:
        s = 0.0
        i = 0
        while i < len(x):
            s = s + row[i] * x[i]
            i = i + 1
        out.append(s)
    return out

def _gpt_softmax(logits):
    max_val = logits[0]
    for v in logits:
        if v > max_val:
            max_val = v
    exps = []
    total = 0.0
    for v in logits:
        e = _m_exp(v - max_val)
        exps.append(e)
        total = total + e
    result = []
    for e in exps:
        result.append(e / total)
    return result

def _gpt_rmsnorm(x):
    n = len(x)
    ms = 0.0
    for v in x:
        ms = ms + v * v
    ms = ms / n
    scale = 1.0 / _m_sqrt(ms + 1e-5)
    result = []
    for v in x:
        result.append(v * scale)
    return result

def _gpt_relu(x):
    result = []
    for v in x:
        if v > 0.0:
            result.append(v)
        else:
            result.append(0.0)
    return result

def _gpt_forward(token, pos, keys, values, sd):
    tok_emb = sd["wte"][token]
    pos_emb = sd["wpe"][pos]
    x = []
    i = 0
    while i < len(tok_emb):
        x.append(tok_emb[i] + pos_emb[i])
        i = i + 1
    x = _gpt_rmsnorm(x)

    li = 0
    while li < _GPT_N_LAYER:
        x_res = x
        x = _gpt_rmsnorm(x)
        pfx = "layer" + str(li)
        q = _gpt_linear(x, sd[pfx + ".attn_wq"])
        k = _gpt_linear(x, sd[pfx + ".attn_wk"])
        v = _gpt_linear(x, sd[pfx + ".attn_wv"])
        keys[li].append(k)
        values[li].append(v)

        x_attn = []
        h = 0
        while h < _GPT_N_HEAD:
            hs = h * _GPT_HEAD_DIM
            he = hs + _GPT_HEAD_DIM
            q_h = q[hs:he]
            seq_len = len(keys[li])
            attn_logits = []
            inv_scale = 1.0 / _m_sqrt(float(_GPT_HEAD_DIM))
            t = 0
            while t < seq_len:
                dot = 0.0
                j = 0
                while j < _GPT_HEAD_DIM:
                    dot = dot + q_h[j] * keys[li][t][hs + j]
                    j = j + 1
                attn_logits.append(dot * inv_scale)
                t = t + 1
            attn_w = _gpt_softmax(attn_logits)
            j = 0
            while j < _GPT_HEAD_DIM:
                s = 0.0
                t = 0
                while t < seq_len:
                    s = s + attn_w[t] * values[li][t][hs + j]
                    t = t + 1
                x_attn.append(s)
                j = j + 1
            h = h + 1

        x = _gpt_linear(x_attn, sd[pfx + ".attn_wo"])
        tmp = []
        i = 0
        while i < len(x):
            tmp.append(x[i] + x_res[i])
            i = i + 1
        x = tmp

        x_res = x
        x = _gpt_rmsnorm(x)
        x = _gpt_linear(x, sd[pfx + ".mlp_fc1"])
        x = _gpt_relu(x)
        x = _gpt_linear(x, sd[pfx + ".mlp_fc2"])
        tmp = []
        i = 0
        while i < len(x):
            tmp.append(x[i] + x_res[i])
            i = i + 1
        x = tmp
        li = li + 1

    logits = _gpt_linear(x, sd["lm_head"])
    return logits

def _gpt_sample(probs):
    r = _rng_random()
    cumsum = 0.0
    i = 0
    while i < len(probs):
        cumsum = cumsum + probs[i]
        if r < cumsum:
            return i
        i = i + 1
    return len(probs) - 1

def _gpt_generate(sd, num_samples, temperature):
    bos = _GPT_VOCAB_SIZE - 1
    results = []
    sample_i = 0
    while sample_i < num_samples:
        keys = []
        values = []
        li = 0
        while li < _GPT_N_LAYER:
            keys.append([])
            values.append([])
            li = li + 1
        token = bos
        chars = []
        pos = 0
        while pos < _GPT_BLOCK_SIZE:
            logits = _gpt_forward(token, pos, keys, values, sd)
            scaled = []
            for l in logits:
                scaled.append(l / temperature)
            probs = _gpt_softmax(scaled)
            token = _gpt_sample(probs)
            if token == bos:
                break
            chars.append(_GPT_UCHARS[token])
            pos = pos + 1
        results.append("".join(chars))
        sample_i = sample_i + 1
    return results

