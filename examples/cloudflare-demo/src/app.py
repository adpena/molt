def _m_sqrt(x: float) -> float:
    if x <= 0.0:
        return 0.0
    r: float = x
    i: int = 0
    while i < 10:
        r = 0.5 * (r + x / r)
        i = i + 1
    return r

def _m_exp(x: float) -> float:
    if x > 88.0:
        return 1e308
    if x < -88.0:
        return 0.0
    n: int = int(x * 1.4426950408889634 + (0.5 if x >= 0.0 else -0.5))
    r: float = x - n * 0.6931471805599453
    r2: float = r * r
    p: float = 1680.0 + 840.0 * r + 180.0 * r2 + 20.0 * r * r2 + r2 * r2
    q: float = 1680.0 - 840.0 * r + 180.0 * r2 - 20.0 * r * r2 + r2 * r2
    e: float = p / q
    if n > 0:
        i: int = 0
        while i < n:
            e = e * 2.0
            i = i + 1
    elif n < 0:
        i = 0
        while i < -n:
            e = e * 0.5
            i = i + 1
    return e

