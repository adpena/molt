def _run(
    cmd: list[str], *, cwd: Path | None = None
) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        cmd,
        cwd=cwd,
        check=False,
        capture_output=True,
        text=True,
    )


def _probe_command(
    cmd: list[str],
    *,
    cwd: Path | None = None,
    timeout_seconds: int = 10,
    env: dict[str, str] | None = None,
) -> dict[str, Any]:
    try:
        proc = subprocess.run(
            cmd,
            cwd=cwd,
            check=False,
            capture_output=True,
            text=True,
            timeout=timeout_seconds,
            env=env,
        )
    except Exception as exc:
        return {
            "ok": False,
            "returncode": None,
            "stdout": "",
            "stderr": str(exc),
            "error": str(exc),
        }
    return {
        "ok": proc.returncode == 0,
        "returncode": int(proc.returncode),
        "stdout": (proc.stdout or "").strip()[-500:],
        "stderr": (proc.stderr or "").strip()[-500:],
    }


def _require_command(name: str) -> str:
    resolved = shutil.which(name)
    if not resolved:
        raise RuntimeError(f"required command not found on PATH: {name}")
    return resolved


def _parse_env_file(path: Path) -> dict[str, str]:
    if not path.exists():
        return {}
    env: dict[str, str] = {}
    for raw in path.read_text(encoding="utf-8").splitlines():
        line = raw.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        env[key.strip()] = value.strip().strip('"').strip("'")
    return env


def _write_env_file(path: Path, env: dict[str, str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = ["# Auto-generated by tools/symphony_bootstrap.py"]
    for key in sorted(env):
        value = env[key]
        rendered = shlex.quote(value) if any(ch.isspace() for ch in value) else value
        lines.append(f"{key}={rendered}")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def _default_quint_node_fallback() -> str:
    for candidate in (
        Path("/opt/homebrew/opt/node@22/bin/node"),
        Path("/usr/local/opt/node@22/bin/node"),
    ):
        if candidate.exists():
            return str(candidate)
    return "npx -y node@22"


def _default_java_home() -> str | None:
    for env_key in ("JAVA_HOME", "MOLT_JAVA_HOME"):
        raw = str(os.environ.get(env_key) or "").strip()
        if raw and (Path(raw) / "bin" / "java").exists():
            return raw
    for candidate in (
        Path("/opt/homebrew/opt/openjdk@21/libexec/openjdk.jdk/Contents/Home"),
        Path("/opt/homebrew/opt/openjdk/libexec/openjdk.jdk/Contents/Home"),
        Path("/usr/local/opt/openjdk@21/libexec/openjdk.jdk/Contents/Home"),
        Path("/usr/local/opt/openjdk/libexec/openjdk.jdk/Contents/Home"),
        Path("/Library/Java/JavaVirtualMachines/openjdk-21.jdk/Contents/Home"),
        Path("/Library/Java/JavaVirtualMachines/temurin-21.jdk/Contents/Home"),
    ):
        if (candidate / "bin" / "java").exists():
            return str(candidate)
    return None


def _java_probe_env(java_home: str | None) -> dict[str, str] | None:
    if not java_home:
        return None
    env = dict(os.environ)
    env["JAVA_HOME"] = java_home
    java_bin_dir = str(Path(java_home) / "bin")
    current_path = env.get("PATH", "")
    path_parts = [part for part in current_path.split(os.pathsep) if part]
    if java_bin_dir not in path_parts:
        env["PATH"] = (
            f"{java_bin_dir}{os.pathsep}{current_path}"
            if current_path
            else java_bin_dir
        )
    return env


def _formal_toolchain_report(repo_root: Path, env_file: Path) -> dict[str, Any]:
    env = _parse_env_file(env_file)
    fallback_raw = (
        str(env.get("MOLT_QUINT_NODE_FALLBACK") or "").strip()
        or str(os.environ.get("MOLT_QUINT_NODE_FALLBACK") or "").strip()
        or _default_quint_node_fallback()
    )
    java_home = (
        str(env.get("JAVA_HOME") or "").strip()
        or str(env.get("MOLT_JAVA_HOME") or "").strip()
        or _default_java_home()
    )
    if java_home and not (Path(java_home) / "bin" / "java").exists():
        java_home = None
    probe_env = _java_probe_env(java_home)
    fallback_parts = [part for part in shlex.split(fallback_raw) if part]

    node_bin = shutil.which("node")
    quint_bin = shutil.which("quint")
    lake_bin = shutil.which("lake")
    java_bin = str(Path(java_home) / "bin" / "java") if java_home else None
    if not java_bin or not Path(java_bin).exists():
        java_bin = shutil.which("java")
    if not lake_bin:
        elan_lake = Path.home() / ".elan" / "bin" / "lake"
        if elan_lake.exists():
            lake_bin = str(elan_lake)

    node_version = _probe_command([node_bin, "--version"]) if node_bin else None
    java_version = (
        _probe_command([java_bin, "-version"], env=probe_env) if java_bin else None
    )
    formal_lean_dir = repo_root / "formal" / "lean"
    lake_probe = (
        _probe_command([lake_bin, "--version"], cwd=formal_lean_dir)
        if lake_bin and formal_lean_dir.exists()
        else (_probe_command([lake_bin, "--version"]) if lake_bin else None)
    )

    quint_direct = (
        _probe_command([quint_bin, "--version"], env=probe_env) if quint_bin else None
    )
    quint_fallback = None
    if quint_bin and fallback_parts:
        quint_fallback = _probe_command(
            [*fallback_parts, quint_bin, "--version"],
            env=probe_env,
        )

    direct_ok = bool(quint_direct and quint_direct.get("ok"))
    fallback_ok = bool(quint_fallback and quint_fallback.get("ok"))
    lake_ok = bool(lake_probe and lake_probe.get("ok"))
    java_ok = bool(java_version and java_version.get("ok"))
    if not quint_bin or not lake_bin:
        status = "warn"
    elif direct_ok and lake_ok and java_ok:
        status = "pass"
    elif fallback_ok and lake_ok and java_ok:
        status = "warn"
    else:
        status = "warn"

    return {
        "status": status,
        "node": {
            "path": node_bin,
            "probe": node_version,
        },
        "java": {
            "home": java_home,
            "path": java_bin,
            "probe": java_version,
        },
        "quint": {
            "path": quint_bin,
            "direct_probe": quint_direct,
            "fallback_command": fallback_parts,
            "fallback_probe": quint_fallback,
        },
        "lake": {
            "path": lake_bin,
            "probe": lake_probe,
        },
    }


def _git_origin(repo_root: Path) -> str | None:
    proc = _run(["git", "config", "--get", "remote.origin.url"], cwd=repo_root)
    if proc.returncode != 0:
        return None
    value = proc.stdout.strip()
    return value or None


def _ensure_linear_mcp() -> dict[str, Any]:
    list_proc = _run(["codex", "mcp", "list"])
    if list_proc.returncode != 0:
        raise RuntimeError(f"codex mcp list failed: {list_proc.stderr.strip()}")

    configured = False
    for line in list_proc.stdout.splitlines():
        if line.strip().lower().startswith(f"{LINEAR_MCP_NAME} "):
            configured = True
            break

    actions: list[str] = []
    if not configured:
        add_proc = _run(["codex", "mcp", "add", LINEAR_MCP_NAME, LINEAR_MCP_URL])
        if add_proc.returncode != 0:
            raise RuntimeError(
                f"codex mcp add linear failed: {add_proc.stderr.strip()}"
            )
        actions.append("added")

    get_proc = _run(["codex", "mcp", "get", LINEAR_MCP_NAME])
    if get_proc.returncode != 0:
        raise RuntimeError(f"codex mcp get linear failed: {get_proc.stderr.strip()}")
    details = get_proc.stdout
    enabled = "enabled: true" in details.lower()

    # Best-effort auth detection from table output.
    auth_state = "unknown"
    for line in list_proc.stdout.splitlines():
        if line.strip().lower().startswith(f"{LINEAR_MCP_NAME} "):
            if "oauth" in line.lower():
                auth_state = "oauth"
            elif "api key" in line.lower():
                auth_state = "api_key"
            else:
                auth_state = "missing"
            break

    return {
        "configured": True,
        "enabled": enabled,
        "auth_state": auth_state,
        "actions": actions,
    }


