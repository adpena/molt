import json
import os
import shutil
import subprocess
import sys
from pathlib import Path
from typing import Any


DEFAULT_EXT_ROOT = Path("/Volumes/APDataStore/Molt")
DEFAULT_ENV_FILE = Path("ops/linear/runtime/symphony.env")
LINEAR_MCP_NAME = "linear"
LINEAR_MCP_URL = "https://mcp.linear.app/mcp"


def _run(
    cmd: list[str], *, cwd: Path | None = None
) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        cmd,
        cwd=cwd,
        check=False,
        capture_output=True,
        text=True,
    )


def _require_command(name: str) -> str:
    resolved = shutil.which(name)
    if not resolved:
        raise RuntimeError(f"required command not found on PATH: {name}")
    return resolved


def _parse_env_file(path: Path) -> dict[str, str]:
    if not path.exists():
        return {}
    env: dict[str, str] = {}
    for raw in path.read_text(encoding="utf-8").splitlines():
        line = raw.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        env[key.strip()] = value.strip().strip('"').strip("'")
    return env


def _write_env_file(path: Path, env: dict[str, str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = ["# Auto-generated by tools/symphony_bootstrap.py"]
    for key in sorted(env):
        value = env[key]
        lines.append(f"{key}={value}")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def _git_origin(repo_root: Path) -> str | None:
    proc = _run(["git", "config", "--get", "remote.origin.url"], cwd=repo_root)
    if proc.returncode != 0:
        return None
    value = proc.stdout.strip()
    return value or None


def _ensure_linear_mcp() -> dict[str, Any]:
    list_proc = _run(["codex", "mcp", "list"])
    if list_proc.returncode != 0:
        raise RuntimeError(f"codex mcp list failed: {list_proc.stderr.strip()}")

    configured = False
    for line in list_proc.stdout.splitlines():
        if line.strip().lower().startswith(f"{LINEAR_MCP_NAME} "):
            configured = True
            break

    actions: list[str] = []
    if not configured:
        add_proc = _run(["codex", "mcp", "add", LINEAR_MCP_NAME, LINEAR_MCP_URL])
        if add_proc.returncode != 0:
            raise RuntimeError(
                f"codex mcp add linear failed: {add_proc.stderr.strip()}"
            )
        actions.append("added")

    get_proc = _run(["codex", "mcp", "get", LINEAR_MCP_NAME])
    if get_proc.returncode != 0:
        raise RuntimeError(f"codex mcp get linear failed: {get_proc.stderr.strip()}")
    details = get_proc.stdout
    enabled = "enabled: true" in details.lower()

    # Best-effort auth detection from table output.
    auth_state = "unknown"
    for line in list_proc.stdout.splitlines():
        if line.strip().lower().startswith(f"{LINEAR_MCP_NAME} "):
            if "oauth" in line.lower():
                auth_state = "oauth"
            elif "api key" in line.lower():
                auth_state = "api_key"
            else:
                auth_state = "missing"
            break

    return {
        "configured": True,
        "enabled": enabled,
        "auth_state": auth_state,
        "actions": actions,
    }


def _sync_env_defaults(
    *,
    repo_root: Path,
    ext_root: Path,
    env_file: Path,
    project_slug: str | None,
    source_repo_url: str | None,
) -> dict[str, Any]:
    current = _parse_env_file(env_file)
    merged = dict(current)

    merged.setdefault("MOLT_EXT_ROOT", str(ext_root))
    merged.setdefault("CARGO_TARGET_DIR", str(ext_root / "cargo-target"))
    merged.setdefault(
        "MOLT_DIFF_CARGO_TARGET_DIR",
        merged["CARGO_TARGET_DIR"],
    )
    merged.setdefault("MOLT_CACHE", str(ext_root / "molt_cache"))
    merged.setdefault("MOLT_DIFF_ROOT", str(ext_root / "diff"))
    merged.setdefault("MOLT_DIFF_TMPDIR", str(ext_root / "tmp"))
    merged.setdefault("UV_CACHE_DIR", str(ext_root / "uv-cache"))
    merged.setdefault("TMPDIR", str(ext_root / "tmp"))

    if project_slug:
        merged.setdefault("MOLT_LINEAR_PROJECT_SLUG", project_slug)

    repo_url = source_repo_url or _git_origin(repo_root)
    if repo_url:
        merged.setdefault("MOLT_SOURCE_REPO_URL", repo_url)

    merged.setdefault("MOLT_SYMPHONY_SYNC_REMOTE", "origin")
    merged.setdefault("MOLT_SYMPHONY_SYNC_BRANCH", "main")
    merged.setdefault("MOLT_SYMPHONY_AUTOMERGE_ALLOWED_AUTHORS", "adpena,symphony")
    merged.setdefault("MOLT_SYMPHONY_TOOL_STATE_DETAIL", "compact")
    merged.setdefault("MOLT_SYMPHONY_MAX_CODEX_EVENT_COUNTERS", "64")
    merged.setdefault("MOLT_SYMPHONY_DURABLE_MEMORY", "1")
    merged.setdefault(
        "MOLT_SYMPHONY_DURABLE_ROOT",
        str(ext_root / "logs" / "symphony" / "durable_memory"),
    )
    merged.setdefault("MOLT_SYMPHONY_DURABLE_SYNC_SECONDS", "180")
    merged.setdefault(
        "MOLT_SYMPHONY_API_TOKEN_FILE",
        str(ext_root / "logs" / "symphony" / "secrets" / "dashboard_api_token"),
    )
    merged.setdefault("MOLT_SYMPHONY_ENFORCE_ORIGIN", "1")
    merged.setdefault("MOLT_SYMPHONY_REQUIRE_CSRF_HEADER", "1")
    merged.setdefault("MOLT_SYMPHONY_SECURITY_PROFILE", "local")
    merged.setdefault("MOLT_SYMPHONY_BIND_HOST", "127.0.0.1")
    merged.setdefault("MOLT_SYMPHONY_ALLOW_NONLOCAL_BIND", "0")
    merged.setdefault("MOLT_SYMPHONY_ALLOW_QUERY_TOKEN", "1")
    merged.setdefault("MOLT_SYMPHONY_DISABLE_DASHBOARD_UI", "0")
    merged.setdefault(
        "MOLT_SYMPHONY_SECURITY_EVENTS_FILE",
        str(ext_root / "logs" / "symphony" / "security" / "events.jsonl"),
    )
    merged.setdefault("MOLT_SYMPHONY_MAX_HTTP_CONNECTIONS", "96")
    merged.setdefault("MOLT_SYMPHONY_MAX_STREAM_CLIENTS", "16")
    merged.setdefault("MOLT_SYMPHONY_STREAM_MAX_AGE_SECONDS", "300")
    merged.setdefault("MOLT_SYMPHONY_HTTP_RATE_LIMIT_MAX_REQUESTS", "240")
    merged.setdefault("MOLT_SYMPHONY_HTTP_RATE_LIMIT_WINDOW_SECONDS", "60")
    merged.setdefault("MOLT_SYMPHONY_EVENT_QUEUE_MAX", "8192")
    merged.setdefault("MOLT_SYMPHONY_EVENT_QUEUE_DROP_LOG_INTERVAL", "250")

    if os.environ.get("LINEAR_API_KEY"):
        merged.setdefault("LINEAR_API_KEY", os.environ["LINEAR_API_KEY"])

    _write_env_file(env_file, merged)
    missing = [
        key
        for key in (
            "LINEAR_API_KEY",
            "MOLT_LINEAR_PROJECT_SLUG",
            "MOLT_SOURCE_REPO_URL",
        )
        if not merged.get(key)
    ]

    return {
        "env_file": str(env_file),
        "missing_required": missing,
        "keys_written": sorted(merged.keys()),
    }


def _ensure_ext_dirs(ext_root: Path) -> list[str]:
    dirs = [
        ext_root,
        ext_root / "tmp",
        ext_root / "uv-cache",
        ext_root / "cargo-target",
        ext_root / "molt_cache",
        ext_root / "diff",
        ext_root / "symphony_workspaces",
        ext_root / "logs" / "symphony",
        ext_root / "logs" / "symphony" / "secrets",
        ext_root / "logs" / "symphony" / "security",
    ]
    created: list[str] = []
    for path in dirs:
        if path.exists():
            continue
        path.mkdir(parents=True, exist_ok=True)
        created.append(str(path))
    return created


def _configure_lin_cli(
    *, env_file: Path, home_dir: Path | None = None
) -> dict[str, Any]:
    lin_path = shutil.which("lin")
    if not lin_path:
        return {
            "configured": False,
            "reason": "lin_not_found",
            "lin_path": None,
        }

    api_key = os.environ.get("LINEAR_API_KEY", "").strip()
    if not api_key:
        api_key = _parse_env_file(env_file).get("LINEAR_API_KEY", "").strip()  # secret-guard: allow
    if not api_key:
        return {
            "configured": False,
            "reason": "missing_linear_api_key",
            "lin_path": lin_path,
        }

    target_home = home_dir or Path.home()
    lin_dir = target_home / ".lin"
    lin_dir.mkdir(parents=True, exist_ok=True)
    try:
        os.chmod(lin_dir, 0o700)
    except OSError:
        pass

    store_file = lin_dir / "store.apiKey.json"
    previous_key: str | None = None
    if store_file.exists():
        try:
            parsed = json.loads(store_file.read_text(encoding="utf-8"))
            if isinstance(parsed, str):
                previous_key = parsed
        except (json.JSONDecodeError, OSError):
            previous_key = None

    store_file.write_text(json.dumps(api_key), encoding="utf-8")
    try:
        os.chmod(store_file, 0o600)
    except OSError:
        pass

    fingerprint = hashlib.blake2s(api_key.encode("utf-8"), digest_size=6).hexdigest()
    return {
        "configured": True,
        "reason": "ok",
        "lin_path": lin_path,
        "store_file": str(store_file),
        "changed": previous_key != api_key,
        "api_key_fingerprint": fingerprint,
    }


def _ensure_git_hooks_path(
    *, repo_root: Path, force_path: bool
) -> dict[str, str | bool | None]:
    hooks_path = ".githooks"
    get_proc = _run(
        ["git", "config", "--local", "--get", "core.hooksPath"], cwd=repo_root
    )
    current = get_proc.stdout.strip() if get_proc.returncode == 0 else ""
    normalized = current.strip()
    should_set = force_path or normalized in {"", ".git/hooks"}
    if not should_set:
        return {
            "ok": True,
            "updated": False,
            "path": normalized,
            "reason": "preserved_existing",
        }
    set_proc = _run(
        ["git", "config", "--local", "core.hooksPath", hooks_path],
        cwd=repo_root,
    )
    return {
        "ok": set_proc.returncode == 0,
        "updated": set_proc.returncode == 0,
        "path": hooks_path if set_proc.returncode == 0 else normalized or None,
        "reason": "set_default" if set_proc.returncode == 0 else "git_config_failed",
        "stderr": set_proc.stderr.strip() if set_proc.returncode != 0 else "",
    }


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Bootstrap Symphony + Linear harness tooling (MCP, env, external dirs, "
            "optional launchd install) with minimal repeated manual setup."
        )
    )
    parser.add_argument("--repo-root", default=".")
    parser.add_argument("--ext-root", default=str(DEFAULT_EXT_ROOT))
    parser.add_argument("--env-file", default=str(DEFAULT_ENV_FILE))
    parser.add_argument("--project-slug", default=None)
    parser.add_argument("--source-repo-url", default=None)
    parser.add_argument("--install-launchd", action="store_true")
    parser.add_argument("--launchd-port", type=int, default=8089)
    parser.add_argument("--force-git-hooks-path", action="store_true")
    parser.add_argument("--python", default=sys.executable)
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    repo_root = Path(args.repo_root).resolve()
    ext_root = Path(args.ext_root).expanduser().resolve()
    env_file = Path(args.env_file).expanduser().resolve()

    tool_paths = {
        "codex": _require_command("codex"),
        "rg": _require_command("rg"),
        "uv": _require_command("uv"),
        "python": _require_command("python3"),
    }

    if not ext_root.exists():
        raise RuntimeError(
            f"external root not found: {ext_root}. Mount it before bootstrap."
        )

    created_dirs = _ensure_ext_dirs(ext_root)
    mcp = _ensure_linear_mcp()
    env_summary = _sync_env_defaults(
        repo_root=repo_root,
        ext_root=ext_root,
        env_file=env_file,
        project_slug=args.project_slug,
        source_repo_url=args.source_repo_url,
    )
    hooks_summary = _ensure_git_hooks_path(
        repo_root=repo_root,
        force_path=bool(args.force_git_hooks_path),
    )
    lin_cli_summary = _configure_lin_cli(env_file=env_file)

    launchd_summary: dict[str, Any] = {"installed": False}
    if args.install_launchd:
        proc = _run(
            [
                args.python,
                "tools/symphony_launchd.py",
                "install",
                "--repo-root",
                str(repo_root),
                "--port",
                str(args.launchd_port),
                "--env-file",
                str(env_file),
                "--ext-root",
                str(ext_root),
            ],
            cwd=repo_root,
        )
        launchd_summary = {
            "installed": proc.returncode == 0,
            "returncode": proc.returncode,
            "stdout": proc.stdout.strip(),
            "stderr": proc.stderr.strip(),
        }

    output = {
        "repo_root": str(repo_root),
        "ext_root": str(ext_root),
        "tool_paths": tool_paths,
        "created_dirs": created_dirs,
        "mcp_linear": mcp,
        "env": env_summary,
        "git_hooks": hooks_summary,
        "lin_cli": lin_cli_summary,
        "launchd": launchd_summary,
        "notes": [
            "LINEAR_API_KEY is still required in env file for direct GraphQL tracker access.",
            "lin CLI API key store is auto-seeded from LINEAR_API_KEY when lin is installed.",
            "If mcp_linear.auth_state is missing, run: codex mcp login linear",
        ],
    }
    print(json.dumps(output, indent=2, sort_keys=True))

    missing = env_summary["missing_required"]
    if missing:
        return 2
    if args.install_launchd and not launchd_summary.get("installed"):
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
