def _ensure_ext_dirs(ext_root: Path) -> list[str]:
    dirs = [
        ext_root,
        ext_root / "tmp",
        ext_root / "uv-cache",
        ext_root / "cargo-target",
        ext_root / "molt_cache",
        ext_root / "diff",
        ext_root / "symphony_workspaces",
        ext_root / "logs" / "symphony",
        ext_root / "logs" / "symphony" / "secrets",
        ext_root / "logs" / "symphony" / "security",
    ]
    created: list[str] = []
    for path in dirs:
        if path.exists():
            continue
        path.mkdir(parents=True, exist_ok=True)
        created.append(str(path))
    return created


def _configure_lin_cli(
    *, env_file: Path, home_dir: Path | None = None
) -> dict[str, Any]:
    lin_path = shutil.which("lin")
    if not lin_path:
        return {
            "configured": False,
            "reason": "lin_not_found",
            "lin_path": None,
        }

    api_key = os.environ.get("LINEAR_API_KEY", "").strip()
    if not api_key:
        api_key = (
            _parse_env_file(env_file).get("LINEAR_API_KEY", "").strip()
        )  # secret-guard: allow
    if not api_key:
        return {
            "configured": False,
            "reason": "missing_linear_api_key",
            "lin_path": lin_path,
        }

    target_home = home_dir or Path.home()
    lin_dir = target_home / ".lin"
    lin_dir.mkdir(parents=True, exist_ok=True)
    try:
        os.chmod(lin_dir, 0o700)
    except OSError:
        pass

    store_file = lin_dir / "store.apiKey.json"
    previous_key: str | None = None
    if store_file.exists():
        try:
            parsed = json.loads(store_file.read_text(encoding="utf-8"))
            if isinstance(parsed, str):
                previous_key = parsed
        except (json.JSONDecodeError, OSError):
            previous_key = None

    store_file.write_text(json.dumps(api_key), encoding="utf-8")
    try:
        os.chmod(store_file, 0o600)
    except OSError:
        pass

    fingerprint = hashlib.blake2s(api_key.encode("utf-8"), digest_size=6).hexdigest()
    return {
        "configured": True,
        "reason": "ok",
        "lin_path": lin_path,
        "store_file": str(store_file),
        "changed": previous_key != api_key,
        "api_key_fingerprint": fingerprint,
    }


def _ensure_git_hooks_path(
    *, repo_root: Path, force_path: bool
) -> dict[str, str | bool | None]:
    hooks_path = ".githooks"
    get_proc = _run(
        ["git", "config", "--local", "--get", "core.hooksPath"], cwd=repo_root
    )
    current = get_proc.stdout.strip() if get_proc.returncode == 0 else ""
    normalized = current.strip()
    should_set = force_path or normalized in {"", ".git/hooks"}
    if not should_set:
        return {
            "ok": True,
            "updated": False,
            "path": normalized,
            "reason": "preserved_existing",
        }
    set_proc = _run(
        ["git", "config", "--local", "core.hooksPath", hooks_path],
        cwd=repo_root,
    )
    return {
        "ok": set_proc.returncode == 0,
        "updated": set_proc.returncode == 0,
        "path": hooks_path if set_proc.returncode == 0 else normalized or None,
        "reason": "set_default" if set_proc.returncode == 0 else "git_config_failed",
        "stderr": set_proc.stderr.strip() if set_proc.returncode != 0 else "",
    }


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Bootstrap Symphony + Linear harness tooling (MCP, env, external dirs, "
            "optional launchd install) with minimal repeated manual setup."
        )
    )
    parser.add_argument("--repo-root", default=".")
    parser.add_argument("--ext-root", default=str(DEFAULT_EXT_ROOT))
    parser.add_argument("--env-file", default=str(DEFAULT_ENV_FILE))
    parser.add_argument("--project-slug", default=None)
    parser.add_argument("--source-repo-url", default=None)
    parser.add_argument("--install-launchd", action="store_true")
    parser.add_argument("--launchd-port", type=int, default=8089)
    parser.add_argument("--force-git-hooks-path", action="store_true")
    parser.add_argument("--python", default=sys.executable)
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    repo_root = Path(args.repo_root).resolve()
    ext_root = Path(args.ext_root).expanduser().resolve()
    env_file = Path(args.env_file).expanduser().resolve()

    tool_paths = {
        "codex": _require_command("codex"),
        "rg": _require_command("rg"),
        "uv": _require_command("uv"),
        "python": _require_command("python3"),
    }

    if not ext_root.exists():
        raise RuntimeError(
            f"external root not found: {ext_root}. Mount it before bootstrap."
        )

    created_dirs = _ensure_ext_dirs(ext_root)
    mcp = _ensure_linear_mcp()
    env_summary = _sync_env_defaults(
        repo_root=repo_root,
        ext_root=ext_root,
        env_file=env_file,
        project_slug=args.project_slug,
        source_repo_url=args.source_repo_url,
    )
    formal_toolchain = _formal_toolchain_report(repo_root, env_file)
    hooks_summary = _ensure_git_hooks_path(
        repo_root=repo_root,
        force_path=bool(args.force_git_hooks_path),
    )
    lin_cli_summary = _configure_lin_cli(env_file=env_file)

    launchd_summary: dict[str, Any] = {"installed": False}
    if args.install_launchd:
        proc = _run(
            [
                args.python,
                "tools/symphony_launchd.py",
                "install",
                "--repo-root",
                str(repo_root),
                "--port",
                str(args.launchd_port),
                "--env-file",
                str(env_file),
                "--ext-root",
                str(ext_root),
            ],
            cwd=repo_root,
        )
        launchd_summary = {
            "installed": proc.returncode == 0,
            "returncode": proc.returncode,
            "stdout": proc.stdout.strip(),
            "stderr": proc.stderr.strip(),
        }

    output = {
        "repo_root": str(repo_root),
        "ext_root": str(ext_root),
        "tool_paths": tool_paths,
        "created_dirs": created_dirs,
        "mcp_linear": mcp,
        "env": env_summary,
        "formal_toolchain": formal_toolchain,
        "git_hooks": hooks_summary,
        "lin_cli": lin_cli_summary,
        "launchd": launchd_summary,
        "notes": [
            "LINEAR_API_KEY is still required in env file for direct GraphQL tracker access.",
            "lin CLI API key store is auto-seeded from LINEAR_API_KEY when lin is installed.",
            "If mcp_linear.auth_state is missing, run: codex mcp login linear",
            "Optional DSPy routing is controlled by MOLT_SYMPHONY_DSPY_* env keys.",
        ],
    }
    print(json.dumps(output, indent=2, sort_keys=True))

    missing = env_summary["missing_required"]
    if missing:
        return 2
    if args.install_launchd and not launchd_summary.get("installed"):
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
