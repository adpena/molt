        Path(env_values["MOLT_SYMPHONY_STORE_ROOT"]),
        Path(env_values["MOLT_SYMPHONY_LOG_ROOT"]),
        Path(env_values["MOLT_SYMPHONY_STATE_ROOT"]),
        Path(env_values["MOLT_SYMPHONY_ARTIFACT_ROOT"]),
