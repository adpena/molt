def _run_install_step(
    steps: list[dict[str, Any]],
    *,
    name: str,
    command: list[str],
    cwd: Path | None = None,
    timeout: int = 1800,
) -> None:
    proc = _run(command, cwd=cwd, timeout=timeout)
    steps.append(
        {
            "name": name,
            "command": command,
            "ok": proc.returncode == 0,
            "returncode": proc.returncode,
            "stdout_tail": (proc.stdout or "").strip()[-1200:],
            "stderr_tail": (proc.stderr or "").strip()[-1200:],
        }
    )


def _ensure_mcp_entry(name: str, url: str, steps: list[dict[str, Any]]) -> None:
    listing = _run(["codex", "--yolo", "mcp", "list"], timeout=90)
    if listing.returncode != 0:
        steps.append(
            {
                "name": f"mcp_check_{name}",
                "ok": False,
                "detail": (listing.stderr or listing.stdout).strip()
                or "codex mcp list failed",
            }
        )
        return
    text = (listing.stdout or "").lower()
    if name.lower() in text:
        steps.append(
            {"name": f"mcp_check_{name}", "ok": True, "detail": "already configured"}
        )
        return
    add = _run(["codex", "--yolo", "mcp", "add", name, url], timeout=120)
    steps.append(
        {
            "name": f"mcp_add_{name}",
            "ok": add.returncode == 0,
            "returncode": add.returncode,
            "stdout_tail": (add.stdout or "").strip()[-1200:],
            "stderr_tail": (add.stderr or "").strip()[-1200:],
        }
    )


def _tasklist_running(pid: int) -> bool:
    proc = _run(
        ["tasklist", "/FI", f"PID eq {pid}", "/FO", "CSV", "/NH"],
        timeout=20,
    )
    if proc.returncode != 0:
        return False
    row = (proc.stdout or "").strip().lower()
    if not row:
        return False
    return "no tasks are running" not in row


