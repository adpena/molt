@dataclass(slots=True)
class CheckResult:
    name: str
    ok: bool
    detail: str
    fix: str | None = None

    def to_dict(self) -> dict[str, Any]:
        payload: dict[str, Any] = {
            "name": self.name,
            "ok": self.ok,
            "detail": self.detail,
        }
        if self.fix:
            payload["fix"] = self.fix
        return payload


def _utc_now() -> str:
    return datetime.now(UTC).isoformat().replace("+00:00", "Z")


def _run(
    args: list[str],
    *,
    cwd: Path | None = None,
    timeout: int = 600,
    check: bool = False,
    env: dict[str, str] | None = None,
) -> subprocess.CompletedProcess[str]:
    proc = subprocess.run(
        args,
        cwd=str(cwd or REPO_ROOT),
        check=False,
        capture_output=True,
        text=True,
        timeout=timeout,
        env=env,
    )
    if check and proc.returncode != 0:
        cmd = " ".join(shlex.quote(part) for part in args)
        raise RuntimeError(
            f"command_failed rc={proc.returncode}: {cmd}\nstdout:\n{proc.stdout}\n"
            f"stderr:\n{proc.stderr}"
        )
    return proc


def _which(name: str) -> str | None:
    resolved = shutil.which(name)
    return str(resolved) if resolved else None


def _parse_env_file(path: Path) -> dict[str, str]:
    env: dict[str, str] = {}
    if not path.exists():
        return env
    for raw in path.read_text(encoding="utf-8").splitlines():
        line = raw.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        env[key.strip()] = value.strip().strip('"').strip("'")
    return env


def _render_env_value(value: str) -> str:
    if not value:
        return ""
    if any(ch.isspace() for ch in value):
        escaped = value.replace('"', '\\"')
        return f'"{escaped}"'
    return value


def _write_env_file(path: Path, env: dict[str, str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = ["# Auto-generated by tools/symphony_windows_admin.py"]
    for key in sorted(env):
        lines.append(f"{key}={_render_env_value(env[key])}")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def _to_posix(path: Path) -> str:
    return path.as_posix()


def _coerce_ext_root(value: str | None) -> Path:
    raw = (value or "").strip()
    if raw:
        return Path(raw).expanduser()
    existing = (os.environ.get("MOLT_EXT_ROOT") or "").strip()
    if existing:
        return Path(existing).expanduser()
    return DEFAULT_EXT_ROOT


def _coerce_symphony_parent(value: str | None) -> Path:
    raw = (value or "").strip()
    if raw:
        return Path(raw).expanduser()
    existing = (os.environ.get("MOLT_SYMPHONY_PARENT_ROOT") or "").strip()
    if existing:
        return Path(existing).expanduser()
    return DEFAULT_SYMPHONY_PARENT


def _check_py_version(version: str) -> CheckResult:
    py = _which("py")
    if py is None:
        return CheckResult(
            name=f"python_{version}",
            ok=False,
            detail="py launcher missing",
            fix=f"uv python install {version}",
        )
    proc = _run(["py", f"-{version}", "--version"], timeout=30)
    ok = proc.returncode == 0
    detail = (proc.stdout or proc.stderr).strip() or "no version output"
    return CheckResult(
        name=f"python_{version}",
        ok=ok,
        detail=detail,
        fix=f"uv python install {version}" if not ok else None,
    )


def _check_cmd(name: str, *, version_args: list[str] | None = None) -> CheckResult:
    resolved = _which(name)
    if not resolved:
        return CheckResult(
            name=f"cmd_{name}",
            ok=False,
            detail="not found on PATH",
            fix=f"install {name}",
        )
    detail = resolved
    if version_args:
        proc = _run([name, *version_args], timeout=45)
        if proc.returncode == 0:
            detail = (proc.stdout or proc.stderr).strip().splitlines()[0]
        else:
            detail = (
                f"path={resolved}; version check failed rc={proc.returncode}; "
                f"{(proc.stderr or proc.stdout).strip()}"
            )
    return CheckResult(name=f"cmd_{name}", ok=True, detail=detail)


def _check_repo_sync() -> list[CheckResult]:
    results: list[CheckResult] = []
    if not (REPO_ROOT / ".git").exists():
        results.append(
            CheckResult(
                name="repo_root",
                ok=False,
                detail=f"missing git repo: {REPO_ROOT}",
                fix="run from a checked-out Molt repository",
            )
        )
        return results
    fetch = _run(["git", "fetch", "origin", "--prune"], timeout=120)
    if fetch.returncode != 0:
        results.append(
            CheckResult(
                name="git_fetch",
                ok=False,
                detail=(fetch.stderr or fetch.stdout).strip() or "git fetch failed",
                fix="check network + git credentials",
            )
        )
        return results
    head = _run(["git", "rev-parse", "HEAD"], timeout=30)
    main = _run(["git", "rev-parse", "origin/main"], timeout=30)
    ahead = _run(["git", "rev-list", "--left-right", "--count", "HEAD...origin/main"])
    status = _run(["git", "status", "--porcelain"], timeout=30)
    head_sha = head.stdout.strip()
    main_sha = main.stdout.strip()
    ahead_text = ahead.stdout.strip()
    clean = not status.stdout.strip()
    in_sync = bool(head_sha and main_sha and head_sha == main_sha)
    results.append(
        CheckResult(
            name="repo_commit_sync",
            ok=in_sync,
            detail=f"head={head_sha} origin/main={main_sha} ahead_behind={ahead_text}",
            fix="git pull --ff-only origin main" if not in_sync else None,
        )
    )
    results.append(
        CheckResult(
            name="repo_clean",
            ok=clean,
            detail="working tree clean" if clean else "working tree has local edits",
            fix="stash/commit local changes before long-running automation"
            if not clean
            else None,
        )
    )
    return results


def _check_mcp_status() -> list[CheckResult]:
    list_proc = _run(["codex", "--yolo", "mcp", "list"], timeout=60)
    if list_proc.returncode != 0:
        detail = (
            list_proc.stderr or list_proc.stdout
        ).strip() or "codex mcp list failed"
        return [
            CheckResult(
                name="codex_mcp_list",
                ok=False,
                detail=detail,
                fix="verify codex-cli installation and auth",
            )
        ]
    text = (list_proc.stdout or "").lower()
    has_linear = "linear" in text
    has_fleet = "vertigo-fleet" in text or "fleet" in text
    linear_logged_in = "linear" in text and "not logged in" not in text
    results = [
        CheckResult(
            name="codex_mcp_linear_present",
            ok=has_linear,
            detail="linear MCP configured" if has_linear else "linear MCP missing",
            fix=f"codex --yolo mcp add linear {LINEAR_MCP_URL}"
            if not has_linear
            else None,
        ),
        CheckResult(
            name="codex_mcp_fleet_present",
            ok=has_fleet,
            detail="fleet MCP configured" if has_fleet else "fleet MCP missing",
            fix=(
                f"codex --yolo mcp add {DEFAULT_FLEET_MCP_NAME} {DEFAULT_FLEET_MCP_URL}"
                if not has_fleet
                else None
            ),
        ),
        CheckResult(
            name="codex_mcp_linear_auth",
            ok=linear_logged_in,
            detail=(
                "linear MCP authenticated"
                if linear_logged_in
                else "linear MCP not logged in for this shell profile"
            ),
            fix="codex --yolo mcp login linear" if not linear_logged_in else None,
        ),
    ]
    return results


def _check_env_file(env_file: Path) -> list[CheckResult]:
    if not env_file.exists():
        return [
            CheckResult(
                name="symphony_env_file",
                ok=False,
                detail=f"missing env file: {env_file}",
                fix="run repair command to generate env file",
            )
        ]
    env_map = _parse_env_file(env_file)
    missing = [key for key in REQUIRED_ENV_KEYS if not env_map.get(key, "").strip()]
    if missing:
        return [
            CheckResult(
                name="symphony_env_required_keys",
                ok=False,
                detail=f"missing required env keys: {', '.join(missing)}",
                fix=f"edit {env_file} and fill required values",
            )
        ]
    return [
        CheckResult(
            name="symphony_env_required_keys",
            ok=True,
            detail=f"required keys present ({len(REQUIRED_ENV_KEYS)})",
        )
    ]


def _collect_doctor_report(env_file: Path) -> dict[str, Any]:
    checks: list[CheckResult] = []
    checks.append(
        CheckResult(
            name="platform_windows",
            ok=sys.platform.startswith("win"),
            detail=f"platform={sys.platform}",
            fix="run this command on BAT00 (Windows host)",
        )
    )
    checks.extend(
        [
            _check_cmd("git", version_args=["--version"]),
            _check_cmd("uv", version_args=["--version"]),
            _check_cmd("cargo", version_args=["--version"]),
            _check_cmd("rustup", version_args=["--version"]),
            _check_cmd("codex", version_args=["--yolo", "--version"]),
            _check_cmd("winget", version_args=["--version"]),
            _check_cmd("clang", version_args=["--version"]),
            _check_cmd("wasm-ld", version_args=["--version"]),
            _check_cmd("wasm-tools", version_args=["--version"]),
            _check_cmd("wasmtime", version_args=["--version"]),
            _check_py_version("3.12"),
            _check_py_version("3.13"),
            _check_py_version("3.14"),
        ]
    )
    checks.extend(_check_repo_sync())
    checks.extend(_check_env_file(env_file))
    checks.extend(_check_mcp_status())

    required_files = (
        REPO_ROOT / "WORKFLOW.md",
        REPO_ROOT / "tools" / "symphony_run.py",
        REPO_ROOT / ".mcp.json",
    )
    for file_path in required_files:
        checks.append(
            CheckResult(
                name=f"file_{file_path.name}",
                ok=file_path.exists(),
                detail=str(file_path),
                fix=f"restore {file_path.name}" if not file_path.exists() else None,
            )
        )

    failed = [item for item in checks if not item.ok]
    return {
        "generated_at": _utc_now(),
        "repo_root": str(REPO_ROOT),
        "env_file": str(env_file),
        "ok": not failed,
        "failed_count": len(failed),
        "checks": [item.to_dict() for item in checks],
    }


def _print_report(report: dict[str, Any], *, as_json: bool) -> None:
    if as_json:
        print(json.dumps(report, indent=2, sort_keys=True))
        return
    summary = "PASS" if report.get("ok") else "FAIL"
    print(f"symphony_windows_admin doctor: {summary}")
    print(f"repo_root: {report.get('repo_root')}")
    print(f"env_file: {report.get('env_file')}")
    print(f"failed_count: {report.get('failed_count')}")
    for item in report.get("checks", []):
        status = "OK" if item.get("ok") else "FAIL"
        detail = item.get("detail", "")
        print(f"- [{status}] {item.get('name')}: {detail}")
        fix = item.get("fix")
        if fix and not item.get("ok"):
            print(f"  fix: {fix}")


