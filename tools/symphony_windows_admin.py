def _validate_runtime_env_for_start(runtime_env: dict[str, str]) -> str | None:
    missing = [
        key
        for key in ("LINEAR_API_KEY", "MOLT_LINEAR_PROJECT_SLUG")
        if not str(runtime_env.get(key) or "").strip()
    ]
    if missing:
        return f"missing required values in env file: {', '.join(missing)}"
    py312 = _check_py_version("3.12")
    if not py312.ok:
        return "python 3.12 is required; run repair first"
    return None


def cmd_start(args: argparse.Namespace) -> int:
    env_file = Path(args.env_file).expanduser().resolve()
    runtime_env = _runtime_env(env_file)
    validation_error = _validate_runtime_env_for_start(runtime_env)
    if validation_error:
        payload = {
            "ok": False,
            "error": "invalid_runtime_env",
            "detail": validation_error,
        }
        _print_json_or_human(payload, as_json=bool(args.json))
        return 2

    pid_file = _pid_file(runtime_env)
    pid_file.parent.mkdir(parents=True, exist_ok=True)
    existing = _read_pid_payload(pid_file)
    if existing:
        try:
            pid = int(existing.get("pid"))
        except Exception:
            pid = 0
        if pid > 0 and _tasklist_running(pid):
            payload = {
                "ok": True,
                "status": "already_running",
                "pid": pid,
                "pid_file": str(pid_file),
            }
            _print_json_or_human(payload, as_json=bool(args.json))
            return 0

    log_dir = _log_root(runtime_env) / "daemon"
    log_dir.mkdir(parents=True, exist_ok=True)
    log_file = log_dir / "symphony.log"
    command = [
        "uv",
        "run",
        "--python",
        "3.12",
        "python3",
        "tools/symphony_run.py",
        str(Path(args.workflow).as_posix()),
        "--env-file",
        str(env_file),
        "--port",
        str(args.port),
    ]
    if args.exec_mode:
        command.extend(["--exec-mode", str(args.exec_mode)])
    if args.once:
        command.append("--once")

    creationflags = 0
    if hasattr(subprocess, "DETACHED_PROCESS"):
        creationflags |= int(subprocess.DETACHED_PROCESS)
    if hasattr(subprocess, "CREATE_NEW_PROCESS_GROUP"):
        creationflags |= int(subprocess.CREATE_NEW_PROCESS_GROUP)

    with log_file.open("a", encoding="utf-8") as sink:
        sink.write(f"\n[{_utc_now()}] launching: {' '.join(command)}\n")
        sink.flush()
        proc = subprocess.Popen(
            command,
            cwd=str(REPO_ROOT),
            env=runtime_env,
            stdout=sink,
            stderr=sink,
            creationflags=creationflags,
        )

    payload = {
        "pid": int(proc.pid),
        "started_at": _utc_now(),
        "command": command,
        "env_file": str(env_file),
        "port": int(args.port),
        "workflow": str(args.workflow),
        "log_file": str(log_file),
    }
    pid_file.write_text(json.dumps(payload, indent=2, sort_keys=True), encoding="utf-8")

    token: str | None = None
    healthy = False
    health_payload: dict[str, Any] = {}
    deadline = time.time() + max(int(args.wait_seconds), 1)
    while time.time() < deadline:
        if not _tasklist_running(proc.pid):
            break
        token = _read_api_token(runtime_env) or token
        health = _http_json(
            method="GET",
            url=f"http://127.0.0.1:{args.port}/api/v1/health",
            token=token,
            timeout=5,
        )
        if health.get("ok"):
            healthy = True
            health_payload = health
            break
        time.sleep(1)

    result = {
        "ok": healthy,
        "status": "running" if healthy else "starting_unhealthy",
        "pid_file": str(pid_file),
        **payload,
    }
    if health_payload:
        result["health"] = health_payload.get("payload")
    _print_json_or_human(result, as_json=bool(args.json))
    return 0 if healthy else 3


def cmd_stop(args: argparse.Namespace) -> int:
    env_file = Path(args.env_file).expanduser().resolve()
    runtime_env = _runtime_env(env_file)
    pid_file = _pid_file(runtime_env)
    payload = _read_pid_payload(pid_file)
    if not payload:
        result = {"ok": True, "status": "already_stopped", "pid_file": str(pid_file)}
        _print_json_or_human(result, as_json=bool(args.json))
        return 0

    pid = int(payload.get("pid") or 0)
    stopped = True
    kill_proc: subprocess.CompletedProcess[str] | None = None
    if pid > 0 and _tasklist_running(pid):
        kill_proc = _run(["taskkill", "/PID", str(pid), "/T", "/F"], timeout=120)
        stopped = kill_proc.returncode == 0
    if stopped and pid_file.exists():
        pid_file.unlink()
    result = {
        "ok": stopped,
        "status": "stopped" if stopped else "failed",
        "pid": pid,
        "pid_file": str(pid_file),
    }
    if kill_proc is not None:
        result["taskkill"] = {
            "returncode": kill_proc.returncode,
            "stdout": (kill_proc.stdout or "").strip()[-500:],
            "stderr": (kill_proc.stderr or "").strip()[-500:],
        }
    _print_json_or_human(result, as_json=bool(args.json))
    return 0 if stopped else 2


def _status_payload(runtime_env: dict[str, str], port: int) -> dict[str, Any]:
    pid_file = _pid_file(runtime_env)
    pid_payload = _read_pid_payload(pid_file)
    pid = int(pid_payload.get("pid") or 0) if pid_payload else 0
    running = pid > 0 and _tasklist_running(pid)
    token = _read_api_token(runtime_env)
    health = _http_json(
        method="GET",
        url=f"http://127.0.0.1:{port}/api/v1/health",
        token=token,
        timeout=8,
    )
    state = _http_json(
        method="GET",
        url=f"http://127.0.0.1:{port}/api/v1/state",
        token=token,
        timeout=12,
    )
    result = {
        "ok": running and bool(health.get("ok")),
        "running": running,
        "pid": pid,
        "pid_file": str(pid_file),
        "health": health,
    }
    if state.get("ok"):
        state_payload = state.get("payload")
        if isinstance(state_payload, dict):
            result["state"] = _summarize_state_payload(state_payload)
        else:
            result["state"] = {}
    else:
        result["state"] = {
            "ok": False,
            "error": state.get("error"),
            "status": state.get("status"),
        }
    return result


def cmd_status(args: argparse.Namespace) -> int:
    env_file = Path(args.env_file).expanduser().resolve()
    runtime_env = _runtime_env(env_file)
    payload = _status_payload(runtime_env, int(args.port))
    _print_json_or_human(payload, as_json=bool(args.json))
    return 0 if payload.get("ok") else 2


def cmd_telemetry(args: argparse.Namespace) -> int:
    env_file = Path(args.env_file).expanduser().resolve()
    runtime_env = _runtime_env(env_file)
    token = _read_api_token(runtime_env)
    state = _http_json(
        method="GET",
        url=f"http://127.0.0.1:{args.port}/api/v1/state",
        token=token,
        timeout=15,
    )
    if not state.get("ok"):
        payload = {
            "ok": False,
            "status": state.get("status"),
            "error": state.get("error"),
            "payload": state.get("payload"),
        }
        _print_json_or_human(payload, as_json=bool(args.json))
        return 2
    raw_payload = state.get("payload")
    parsed = raw_payload if isinstance(raw_payload, dict) else {}
    output = parsed if args.full else _summarize_state_payload(parsed)
    payload = {"ok": True, "telemetry": output}
    _print_json_or_human(payload, as_json=bool(args.json))
    return 0


